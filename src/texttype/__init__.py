class TextType():
    def __init__(self, org = (0,0), fontFace = 1, fontScale = 1, color = (255,255,255), thickness = 1, lineType = 1, bottomLeftOrigin = False):
        self.org = org
        self.fontFace = fontFace
        self.fontScale = fontScale
        self.color = color
        self.thickness = thickness
        self.lineType = lineType
        self.bottomLeftOrigin = bottomLeftOrigin